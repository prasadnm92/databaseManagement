package assignment;

public class Benefit {
	private int bid;
	private String type;
	
	public Benefit() {}
	
	public int getBid() {
		return this.bid;
	}
	public void setBid(int id) {
		this.bid = id;
	}
	
	public String getType() {
		return this.type;
	}
	public void setType(String t) {
		this.type = t;
	}
}
