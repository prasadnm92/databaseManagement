package assignment;

public class PayPeriod {
	private int ppid;
	private String duration;
	
	public PayPeriod() {}
	
	public int getPpid() {
		return this.ppid;
	}
	public void setPpid(int id) {
		this.ppid = id;
	}
	
	public String getDuration() {
		return this.duration;
	}
	public void setDuration(String dur) {
		this.duration = dur;
	}
}
